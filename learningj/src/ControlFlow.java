public class ControlFlow {

    public static void main(String[] args) {
        int age = 17;

        if (age < 18) {
            System.out.println("You are not old enough for something");
        } else if (age == 69) {
            System.out.println("nice");
        } else {
            System.out.println("You are old enough for something");
        }


    }
}